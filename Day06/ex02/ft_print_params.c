void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	int index;

	index = 0;
	while (str[index] != '\0')
	{
		ft_putchar(str[index]);
		index++;
	}
}

int		main(int argc, char **argv)
{
	int index;

	index = 1;
	while (argc > index)
	{
		ft_putstr(argv[index]);
		ft_putchar('\n');
		++index;
	}
}
